package bean;

public class phoneform {
    public String phone_version;
    public String phone_name;
    public String phone_made;
    public int phone_price;
    public String phone_feature;
    public String phone_pic;
    public int classify;

    public String getPhone_version() {
        return phone_version;
    }
    public void setPhone_version(String phone_version) {
        this.phone_version = phone_version;
    }

    public String getPhone_name() {
        return phone_name;
    }
    public void setPhone_name(String phone_name) {
        this.phone_name = phone_name;
    }

    public String getPhone_made() {
        return phone_made;
    }
    public void setPhone_made(String phone_made) {
        this.phone_made = phone_made;
    }

    public int getPhone_price() {
        return phone_price;
    }
    public void setPhone_price(int phone_price) {
        this.phone_price = phone_price;
    }

    public String getPhone_feature() {
        return phone_feature;
    }
    public void setPhone_feature(String phone_feature) {
        this.phone_feature = phone_feature;
    }

    public String getPhone_pic() {
        return phone_pic;
    }
    public void setPhone_pic(String phone_pic) {
        this.phone_pic = phone_pic;
    }

    public int getClassify() {
        return classify;
    }
    public void setClassify(int classify) {
        this.classify = classify;
    }
}
